    private void remove_customer_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_remove_customer_btnActionPerformed
    
        // first we remove it from table
        // to bring the id value for the needed row to be deleted
        int ind = this.customer_table.getSelectedRow();
        String x = String.valueOf(this.customer_table.getValueAt(ind, 0));
        int id = Integer.parseInt(x);

        model.removeRow(ind);
        this.customer_table.revalidate();
        this.customer_table.repaint();

        // second we need to delete it from the DB
        make_connector();

        try {
           
            con.setAutoCommit(false);
            String q = "delete from customer where customerid='" + id + "'";
            Statement s = con.createStatement();
            s.executeUpdate(q);

            con.commit();
            con.close();
            con = null;

            
             this.remove_customer_btn_enablation();
            
             JOptionPane.showMessageDialog(null, "The Selected Customer Has Been Successfully Deleted From Your Company !", "Customer Deletion", JOptionPane.PLAIN_MESSAGE + JOptionPane.OK_OPTION);

        } 
        catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Error Happens With Your Data Base Connection!", "Data Base Error", JOptionPane.ERROR_MESSAGE + JOptionPane.OK_OPTION);
            System.exit(0);

        }
        
    }//GEN-LAST:event_remove_customer_btnActionPerformed

