    public void show_customers_on_table() {

        make_connector();

        try {
            String fname;
            String lname;
            String phone;
            String email;
            String password;
            Integer customer_id;

            con.setAutoCommit(false);
            String q = "select * from customer";
            Statement s = con.createStatement();
            ResultSet r = s.executeQuery(q);

            //to fetch the rows 
            while (r.next() == true) {
                customer_id = r.getInt("customerid");
                fname = r.getString("fname");
                lname = r.getString("lname");
                phone = r.getString("phone");
                email = r.getString("email");
                password = r.getString("pass_word");
                //we will show this row to the table

                model.addRow(new Object[]{customer_id, fname,lname,phone,email,password});

            }

            con.commit();
            con.close();
            con = null;

        } 
        catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Error Happens With Your Data Base Connection!", "Data Base Error", JOptionPane.ERROR_MESSAGE + JOptionPane.OK_OPTION);
            System.exit(0);

        }

        return;
    }

